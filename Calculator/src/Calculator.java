public class Calculator {
    public double minus(double x, double y){
        double result = x - y;
        return result;
    }
    public double divide(double x, double y) {
        double result = x / y;
        return result;
    }

    public double add(double x, double y){
        return x+y;
    }

    public double multiply(double x, double y) {
        return x*y;
    }
}
